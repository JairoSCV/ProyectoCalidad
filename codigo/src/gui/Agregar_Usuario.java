    void AgregaUsuario(){
        Conexion cc = new Conexion();
        Connection cn = cc.conexion();
        try{
            PreparedStatement pst = cn.prepareStatement("INSERT INTO usuarios_empresa(ID_Usuario,Nombres_usuario,Apellidos_usuario,DNI_usuario,Telefono_usuario,Correo_usuario,Contrasenia_usuario,Cargo_usuario) VALUES(?,?,?,?,?,?,?,?)");
            String ID_usuario = txtID.getText();
            String Nombre_usuario = "";
            String Apellido_usuario = "";
            String DNI_usuario = "";
            String Telefono_usuario = "";
            String Correo_usuario = "";
            String Cargo_usuario = "";
            if(ID_usuario.length()>0){
                Nombre_usuario = txtNombre.getText();
                if(Nombre_usuario.length()>0){
                    Apellido_usuario = txtApellido.getText();
                    if(Apellido_usuario.length()>0){
                        DNI_usuario = txtDni.getText();
                        if(DNI_usuario.length()>0){
                            Telefono_usuario = txtTelefono.getText();
                            if(Telefono_usuario.length()>0){
                                Correo_usuario = txtCorreo.getText();
                                if(Correo_usuario.length()>0){
                                    char[] cont=txtContra.getPassword();
                                    String contra=new String(cont).trim();
                                    
                                    if(contra.length()>0){
                                        char[] confcont=txtConfContra.getPassword();
                                        String confcontra=new String(confcont).trim();
                                        
                                        if(confcontra.length()>0){
                                            if(contra.equals(confcontra)){
                                                Cargo_usuario = comboBox.getSelectedItem().toString().trim();
                                                pst.setString(1,ID_usuario);
                                                pst.setString(2,Nombre_usuario);
                                                pst.setString(3,Apellido_usuario);
                                                pst.setString(4,DNI_usuario);
                                                pst.setString(5,Telefono_usuario);
                                                pst.setString(6,Correo_usuario);
                                                pst.setString(7,contra);
                                                pst.setString(8,Cargo_usuario);

                                            int a = pst.executeUpdate();
                                            if(a>0){
                                                JOptionPane.showMessageDialog(null, "USUARIO AGREGADO");
                                                mostrardatos("");
                                            }else{
                                                JOptionPane.showMessageDialog(null, "ERROR AL AGREGAR");
                                            }
                                            }else{
                                                mensaje("Las contraseñas no coinciden");
                                                txtConfContra.setText("");
                                                txtContra.setText("");
                                                txtContra.grabFocus();
                                            }
                                        }else{
                                            mensaje("Rellene el campo Confirmación Contraseña correctamente");
                                            txtConfContra.setText("");
                                            txtConfContra.grabFocus();
                                        }
                                        
                                    }else{
                    mensaje("Rellene el campo Contraseña correctamente");
                txtContra.setText("");
                txtContra.grabFocus();
                                    }
                                    
                                }else{
                    mensaje("Rellene el campo Correo correctamente");
                txtCorreo.setText("");
                txtCorreo.grabFocus();
                                }
                            }else{
                    mensaje("Rellene el campo Teléfono correctamente");
                txtTelefono.setText("");
                txtTelefono.grabFocus();
                            }
                        }else{
                    mensaje("Rellene el campo DNI correctamente");
                txtDni.setText("");
                txtDni.grabFocus();
                        }
                    }else{
                    mensaje("Rellene el campo Apellido correctamente");
                txtApellido.setText("");
                txtApellido.grabFocus();
                    }
                }else{
                    mensaje("Rellene el campo Nombre correctamente");
                txtNombre.setText("");
                txtNombre.grabFocus();
                }
            }else{
                mensaje("Rellene el campo ID correctamente");
                txtID.setText("");
                txtID.grabFocus();
            }
            
        }catch(Exception e){
        }
    }
    
