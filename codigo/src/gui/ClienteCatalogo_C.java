     private void valideRadios(){
        if(c1.isSelected()){
            cad_C="C0001";
            Seleccionar(cad_C);
        }
        else if(c2.isSelected()){
            cad_C="C0002";
            Seleccionar(cad_C);
        }
        else if(c3.isSelected()){
            cad_C="C0003";
            Seleccionar(cad_C);
        }
        else if(c4.isSelected()){
            cad_C="C0004";
            Seleccionar(cad_C);
        }
        else if(c5.isSelected()){
            cad_C="C0005";
            Seleccionar(cad_C);
        }
        else if(c6.isSelected()){
            cad_C="C0006";
            Seleccionar(cad_C);
        }else{
            JOptionPane.showMessageDialog(this,"SELECCIONE UN ARTÍCULO");
        }
    }
     
