    void LoginCliente_Registrado(){
        Conexion cc = new Conexion();
        Connection cn = cc.conexion();
        
        try{
            String nroboleta=txtBoleta_Cliente.getText().trim();
            
            if(nroboleta.length()>0){
                char[] clave= txtPass_Cliente.getPassword();
		String contraseña=new String(clave).trim();
               
                
                
                if(contraseña.length()>0){
                    String sentencia_buscar = ("SELECT N_boleta, DNI, Contrasenia_boleta, Fecha_pedido FROM pedido WHERE N_boleta = '"+nroboleta+"' AND Contrasenia_boleta = '"+contraseña+"'");
                    
                    PreparedStatement pst = cn.prepareStatement(sentencia_buscar);

                    ResultSet resultado = pst.executeQuery();
                    
                    if(resultado.next()){
                        fecha = resultado.getString("Fecha_pedido");
                        contrasenia = resultado.getString("Contrasenia_boleta");
                        dni_cliente = resultado.getString("DNI");
                        mensaje("¡¡¡BIENVENIDO!!!");
                        dispose();
                        
                        String sentencia_buscar_ArticuloPedido = ("SELECT N_BOLETA, ID_ARTICULO, MEDIDA, MATERIAL, PESO, MONTO_AMORTIGUADO, CANTIDAD, FABRICACION, ENTREGA_TRABAJO, PAGO_ESTADO, ENTREGA_MATERIAL FROM articulo_pedido WHERE N_boleta = '"+nroboleta+"'");
                        PreparedStatement pstArticulo_pedido = cn.prepareStatement(sentencia_buscar_ArticuloPedido);
                        
                        ResultSet resultado_articulopedido = pstArticulo_pedido.executeQuery();
                        
                        if(resultado_articulopedido.next()){
                            N_Boleta = resultado_articulopedido.getString("N_BOLETA");
                            Id_articulo = resultado_articulopedido.getString("ID_ARTICULO");
                            medida = resultado_articulopedido.getString("MEDIDA");
                            material = resultado_articulopedido.getString("MATERIAL");
                            peso = resultado_articulopedido.getDouble("PESO");
                            monto_amortiguado = resultado_articulopedido.getDouble("MONTO_AMORTIGUADO");
                            cantidad = resultado_articulopedido.getInt("CANTIDAD");
                            Estado_Fabricacion = resultado_articulopedido.getString("FABRICACION");
                            Estado_Entrega_Trabajo = resultado_articulopedido.getString("ENTREGA_TRABAJO");
                            Pago_Estado = resultado_articulopedido.getString("PAGO_ESTADO");
                            Entrega_Material = resultado_articulopedido.getString("ENTREGA_MATERIAL");
                            
                            if(Estado_Fabricacion.equals("TERMINADO")){
                                if(Estado_Entrega_Trabajo.equals("ENTREGADO")){
                                    Ventana_info_cliente vc=new Ventana_info_cliente();
                                        vc.setLocationRelativeTo(null);
                                        vc.setVisible(true);
                                }else{
                                    model_articulo = Id_articulo;
                                    Confirmacion c = new Confirmacion();
                                    c.setLocationRelativeTo(null);
                                    c.setVisible(true);
                                }
                            }else{
                                Ventana_info_cliente vc=new Ventana_info_cliente();
                                    vc.setLocationRelativeTo(null);
                                    vc.setVisible(true);
                            }
                        }
                        
                    }else{
                        a--;
				if(a==0){
                                    mensaje("Límite de intentos alcanzados\n\tSe cerrará el sistema");
					System.exit(0);
				}else{
					mensaje("Usuario y/o contraseña incorrecta\n\tIntentos restantes: "+a);
					txtBoleta_Cliente.setText("");
					txtPass_Cliente.setText("");
                                        txtPass_Cliente_Visible.setText("");
                                        txtPass_Cliente_Visible.setVisible(false);
                                        txtPass_Cliente.setVisible(true);
					txtBoleta_Cliente.grabFocus();
				}
                    }
                    
                    
                }else{
                mensaje("INGRESE CONTRASEÑA");
                txtPass_Cliente.setText("");
                txtPass_Cliente.grabFocus();
                }
            }else{
                mensaje("RELLENE EL CAMPO N° BOLETA");
                txtBoleta_Cliente.setText("");
                txtBoleta_Cliente.grabFocus();
            }
        
        }catch(SQLException e){
            mensaje("Se produjo un error: "+e.getMessage());
        }
    }
    
